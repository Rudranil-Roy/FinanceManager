package com.rudra.financemanager.entities;

/**
 * Enumeration representing the type of transaction or category.
 * Can be either INCOME (positive inflow) or EXPENSE (negative outflow).
 */
public enum TransactionTypeEnum {

    /**
     * Income transaction type (representing money gained).
     */
    INCOME,

    /**
     * Expense transaction type (representing money spent).
     */
    EXPENSE
}
